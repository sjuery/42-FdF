#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *dst_start;

	dst_start = dst;
	while (len > 0 && *src)
	{
		*dst++ = *src++;
		len--;
	}
	while (len > 0)
	{
		*dst++ = '\0';
		len--;
	}
	return (dst_start);
}
